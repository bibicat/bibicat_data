package bean;

public class BookArticleBean {

	protected Integer book_id;
	
	protected String book_article_title;
	
	protected String book_article_link;
	
	protected String book_article_content;

	public Integer getBook_id() {
		return book_id;
	}

	public void setBook_id(Integer book_id) {
		this.book_id = book_id;
	}

	public String getBook_article_title() {
		return book_article_title;
	}

	public void setBook_article_title(String book_article_title) {
		this.book_article_title = book_article_title;
	}

	public String getBook_article_link() {
		return book_article_link;
	}

	public void setBook_article_link(String book_article_link) {
		this.book_article_link = book_article_link;
	}

	public String getBook_article_content() {
		return book_article_content;
	}

	public void setBook_article_content(String book_article_content) {
		this.book_article_content = book_article_content;
	}

	
}
